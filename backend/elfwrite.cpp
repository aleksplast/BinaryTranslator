int CreateElfHeader(FILE* elf)
{
    Elf64_Ehdr header = {};
    header.e_ident[EI_MAG0] = 0x7f;
    header.e_ident[EI_MAG1] = 'E';
    header.e_ident[EI_MAG2] = 'L';
    header.e_ident[EI_MAG3] = 'F';
    header.e_ident[EI_CLASS] = ELFCLASS64;
    header.e_ident[EI_DATA] = ELFDATA2LSB;
    header.e_ident[EI_VERSION] = EV_CURRENT;
    header.e_ident[EI_OSABI] = 0x00;
    header.e_type = ET_EXEC;
    header.e_version = EV_CURRENT;
    header.e_machine = 0x3e;        // x86-64 structure
    header.e_entry = 0x400000 + sizeof(Elf64_Ehdr) + sizeof(Elf64_Phdr) + 8;
    header.e_shoff = 0x00;
    header.e_phoff = sizeof(Elf64_Ehdr);
    header.e_flags = 0x00;
    header.e_ehsize = sizeof(Elf64_Ehdr);
    header.e_phentsize = sizeof(Elf64_Phdr);
    header.e_phnum = 0x01;

    fwrite(&header, sizeof(header), 1, elf);

    return NOERR;
}

int CreateProgrammHeader(FILE* elf, int stlib)
{
    Elf64_Phdr head = {};

    head.p_type = 0x01;
    head.p_filesz = 3 * SIZEBUFFER + 0x80 + stlib;
    head.p_memsz = 3 * SIZEBUFFER + 0x80 + stlib;
    head.p_offset = 0x00;
    head.p_paddr = 0x400000;
    head.p_vaddr = 0x400000;
    head.p_align = 0x2000;
    head.p_flags = 0x07;

    fwrite(&head, sizeof(head), 1, elf);

    return NOERR;
}

int WriteBinFunc(FILE* func, FILE* elf)
{
    int size = FileSize(func);
    unsigned char* buffer = (unsigned char*) calloc(size, sizeof(unsigned char));

    fread(buffer, sizeof(char), size, func);

    fwrite(buffer, sizeof(char), size, elf);

    fclose(func);
    free(buffer);

    return NOERR;
}
